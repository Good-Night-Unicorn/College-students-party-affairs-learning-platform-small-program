            xuexiTypesIndex : 0,

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
		/*下拉框*/
			let xuexiTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'xuexi_types',
            }
			let xuexiTypes = await this.$api.page(`dictionary`, xuexiTypesParams);
			this.xuexiTypesOptions = xuexiTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`xuexi`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.xuexiId){
                this.ruleForm.xuexiId = options.xuexiId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            xuexiTypesChange(e) {
                this.xuexiTypesIndex = e.target.value
                this.ruleForm.xuexiValue = this.xuexiTypesOptions[this.xuexiTypesIndex].indexName
                this.ruleForm.xuexiTypes = this.xuexiTypesOptions[this.xuexiTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			xuexiPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.xuexiPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			xuexiFileTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.xuexiFile = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			xuexiVideoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.xuexiVideo = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.xuexiName)) {
                    this.$utils.msg(`在线学习名称不能为空`);
                    return
                }
				if ((!this.ruleForm.xuexiUuidNumber)) {
                    this.$utils.msg(`在线学习编号不能为空`);
                    return
                }
				if ((!this.ruleForm.xuexiPhoto)) {
                    this.$utils.msg(`在线学习照片不能为空`);
                    return
                }
				if ((!this.ruleForm.zanNumber) && (!this.$validate.isIntNumer(this.ruleForm.zanNumber)) && this.ruleForm.zanNumber >0) {
                    this.$utils.msg(`赞不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.caiNumber) && (!this.$validate.isIntNumer(this.ruleForm.caiNumber)) && this.ruleForm.caiNumber >0) {
                    this.$utils.msg(`踩不能为空，不能小于0 格式为数字`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`xuexi`, this.ruleForm);
                } else {
                    await this.$api.save(`xuexi`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
</script>
