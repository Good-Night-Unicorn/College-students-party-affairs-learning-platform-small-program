    import wPicker from "@/components/w-picker/w-picker.vue";

    export default {
        data() {
            return {
                cross: '',
                ro:{
					xuexiName: false,
					xuexiUuidNumber: true,
					xuexiPhoto: false,
					zanNumber: false,
					caiNumber: false,
					xuexiFile: false,
					xuexiVideo: false,
