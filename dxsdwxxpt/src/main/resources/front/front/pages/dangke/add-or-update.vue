<script>
    import wPicker from "@/components/w-picker/w-picker.vue";

    export default {
        data() {
            return {
                cross: '',
                ro:{
					dangkeName: false,
					dangkeUuidNumber: true,
					dangkePhoto: false,
					dangkeAddress: false,
					zanNumber: false,
					caiNumber: false,
					dangkeTypes: false,
					dangkeKucunNumber: false,
					dangkeContent: false,
					dangkeDelete: false,
					insertTime: false,
					createTime: false,
        },
            ruleForm: {
					dangkeName: '',
					dangkeUuidNumber: this.getUUID(),//数字
					dangkePhoto: '',
					dangkeAddress: '',
					zanNumber: '',
					caiNumber: '',
					dangkeTypes: '',//数字
					dangkeValue: '',//数字对应的值
					dangkeKucunNumber: '',
					dangkeContent: '',
					dangkeDelete: '',
					insertTime: '',
					createTime: '',
            },
            // 登陆用户信息
            user: {},
            dangkeTypesOptions: [],
            dangkeTypesIndex : 0,

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
		/*下拉框*/
			let dangkeTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'dangke_types',
            }
			let dangkeTypes = await this.$api.page(`dictionary`, dangkeTypesParams);
			this.dangkeTypesOptions = dangkeTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`dangke`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.dangkeId){
                this.ruleForm.dangkeId = options.dangkeId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            dangkeTypesChange(e) {
                this.dangkeTypesIndex = e.target.value
                this.ruleForm.dangkeValue = this.dangkeTypesOptions[this.dangkeTypesIndex].indexName
                this.ruleForm.dangkeTypes = this.dangkeTypesOptions[this.dangkeTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			dangkePhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.dangkePhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.dangkeName)) {
                    this.$utils.msg(`党课名称不能为空`);
                    return
                }
				if ((!this.ruleForm.dangkeUuidNumber)) {
                    this.$utils.msg(`党课编号不能为空`);
                    return
                }
				if ((!this.ruleForm.dangkePhoto)) {
                    this.$utils.msg(`党课照片不能为空`);
                    return
                }
				if ((!this.ruleForm.dangkeAddress)) {
                    this.$utils.msg(`党课地点不能为空`);
                    return
                }
				if ((!this.ruleForm.zanNumber) && (!this.$validate.isIntNumer(this.ruleForm.zanNumber)) && this.ruleForm.zanNumber >0) {
                    this.$utils.msg(`赞不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.caiNumber) && (!this.$validate.isIntNumer(this.ruleForm.caiNumber)) && this.ruleForm.caiNumber >0) {
                    this.$utils.msg(`踩不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.dangkeKucunNumber) && (!this.$validate.isIntNumer(this.ruleForm.dangkeKucunNumber)) && this.ruleForm.dangkeKucunNumber >0) {
                    this.$utils.msg(`参加人员不能为空，不能小于0 格式为数字`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`dangke`, this.ruleForm);
                } else {
                    await this.$api.save(`dangke`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
</script>
