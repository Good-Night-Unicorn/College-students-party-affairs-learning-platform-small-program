        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
            let dangkeParams = {
                page: 1,
                limit: 100,
            }
            let dangkeData = await this.$api.page(`dangke`, dangkeParams);
            this.dangkeOptions = dangkeData.data.list;
            let yonghuParams = {
                page: 1,
                limit: 100,
            }
            let yonghuData = await this.$api.page(`yonghu`, yonghuParams);
            this.yonghuOptions = yonghuData.data.list;
		/*下拉框*/
			let dangkeOrderTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'dangke_order_types',
            }
			let dangkeOrderTypes = await this.$api.page(`dictionary`, dangkeOrderTypesParams);
			this.dangkeOrderTypesOptions = dangkeOrderTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`dangkeOrder`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.dangkeOrderId){
                this.ruleForm.dangkeOrderId = options.dangkeOrderId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            dangkeChange(e) {
                this.dangkeIndex = e.target.value
                this.ruleForm.dangkeName = this.dangkeOptions[this.dangkeIndex].dangkeName
                this.ruleForm.dangkeId = this.dangkeOptions[this.dangkeIndex].id
            },
            yonghuChange(e) {
                this.yonghuIndex = e.target.value
                this.ruleForm.yonghuName = this.yonghuOptions[this.yonghuIndex].yonghuName
                this.ruleForm.yonghuId = this.yonghuOptions[this.yonghuIndex].id
            },
            // 下拉变化
            dangkeOrderTypesChange(e) {
                this.dangkeOrderTypesIndex = e.target.value
                this.ruleForm.dangkeOrderValue = this.dangkeOrderTypesOptions[this.dangkeOrderTypesIndex].indexName
                this.ruleForm.dangkeOrderTypes = this.dangkeOrderTypesOptions[this.dangkeOrderTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.dangkeOrderUuidNumber)) {
                    this.$utils.msg(`订单编号不能为空`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`dangkeOrder`, this.ruleForm);
                } else {
                    await this.$api.save(`dangkeOrder`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
