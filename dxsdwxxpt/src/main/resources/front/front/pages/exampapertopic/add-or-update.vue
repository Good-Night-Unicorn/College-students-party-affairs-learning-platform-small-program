<script>
    import wPicker from "@/components/w-picker/w-picker.vue";

    export default {
        data() {
            return {
                cross: '',
                ro:{
					exampaperId: false,
					examquestionId: false,
					exampapertopicNumber: false,
					createTime: false,
        },
            ruleForm: {
					exampaperId: '',
					examquestionId: '',
					exampapertopicNumber: '',
					createTime: '',
            },
            // 登陆用户信息
            user: {},
            exampaperOptions: [],//试卷
            exampaperIndex : 0,//试卷下标
            examquestionOptions: [],//试题
            examquestionIndex : 0,//试题下标

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
            let exampaperParams = {
                page: 1,
                limit: 100,
            }
            let exampaperData = await this.$api.page(`exampaper`, exampaperParams);
            this.exampaperOptions = exampaperData.data.list;
            let examquestionParams = {
                page: 1,
                limit: 100,
            }
            let examquestionData = await this.$api.page(`examquestion`, examquestionParams);
            this.examquestionOptions = examquestionData.data.list;


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`exampapertopic`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.exampapertopicId){
                this.ruleForm.exampapertopicId = options.exampapertopicId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            exampaperChange(e) {
                this.exampaperIndex = e.target.value
                this.ruleForm.exampaperName = this.exampaperOptions[this.exampaperIndex].exampaperName
                this.ruleForm.exampaperId = this.exampaperOptions[this.exampaperIndex].id
            },
            examquestionChange(e) {
                this.examquestionIndex = e.target.value
                this.ruleForm.examquestionName = this.examquestionOptions[this.examquestionIndex].examquestionName
                this.ruleForm.examquestionId = this.examquestionOptions[this.examquestionIndex].id
            },
            // 下拉变化
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.exampapertopicNumber) && (!this.$validate.isIntNumer(this.ruleForm.exampapertopicNumber)) && this.ruleForm.exampapertopicNumber >0) {
                    this.$utils.msg(`试题分数不能为空，不能小于0 格式为数字`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`exampapertopic`, this.ruleForm);
                } else {
                    await this.$api.save(`exampapertopic`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
</script>
